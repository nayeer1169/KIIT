public class ArithmeticOpr { //used for adding subtracting etc etc two number
    public static void main(String[] args){
        System.out.println( 9 + 5 );
        System.out.println( 9 - 5 );
        System.out.println( 9 * 5 );
        System.out.println( 9 / 5 );
        System.out.println( 9 % 5 );

        //char + int
        System.out.println( 'A' + 1 ); //Ascii value will  automatically come
        System.out.println( 'a' + 'A' );  //Ascii value of a=97  A=65
    }
}
