import java.util.Scanner;
public class IfelseQues {
    public static void main(String[] args){
        Scanner s = new Scanner(System.in);
        int x = s.nextInt();
        if (x <= 15){
            System.out.println("Inside if   ");
        }else if(x == 15){
            System.out.println("Outside if   ");
            }
        System.out.println(x);
        }
    }

