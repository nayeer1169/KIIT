public class IncreamentDecreament {                                                                  //INCREAMENT
    public static void main(String[] args){
        int a = 10;
        System.out.println(a++);
        a++;                       //post increament
        ++a;                      //pre increament
        System.out.println(a);

//        int b =a++;
//        System.out.println(b);
//        System.out.println(a);

        int b =++a;
        System.out.println(b);
        System.out.println(a);
    }
}
